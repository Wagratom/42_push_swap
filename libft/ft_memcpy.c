#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t len)
{
	unsigned char	*cpy;

	cpy = (unsigned char *)dst;
	while (len--)
		*cpy++ = *(unsigned char *)src++;
	return (dst);
}
